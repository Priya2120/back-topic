# In the above case if the expression evaluates to true the same amount of 
# indented statements(s) following if will be executed and if the 
# expression evaluates to false the same amount of indented statements
# (s) following else will be executed. See the following example. 
# The program will print the second print statement as the value of a is 10.

a=10
if(a>10):
    print("Value of a is greater than 10")
else :
    print("Value of a is 10")






